package lession_02;

public class PostfixPrefixLearning {

        /* ++i Prefix
        *  --i;
        *
        *  i++ Postfix
        *  i--;
         */
        public static void main(String[] args) {
            int num1 = 1;
            int num2 = 2;
            int result = ++num1 + num2++;
            System.out.println(result);
        /*
        * Debug , breakpoint
         */
        }


}
